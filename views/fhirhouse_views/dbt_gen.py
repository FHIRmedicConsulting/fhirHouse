"""Generate dbt-duckdb models from views/definitions/*.ViewDefinition.json (FH-0005 §5).

    python -m fhirhouse_views.dbt_gen

Each ViewDefinition compiles (typed mode) into dbt/models/views/<name>.sql reading the
Silver tier via the delta_table macro; the SoF helper macros are created by the
on-run-start hook (dbt/macros/sof.sql). Generated files carry a header and are
regenerated, never hand-edited — the ViewDefinition stays the only source artifact.
"""
from __future__ import annotations

import json
import pathlib

from .compiler import ViewCompiler

REPO = pathlib.Path(__file__).resolve().parents[2]
DEFINITIONS = REPO / "views" / "definitions"
MODELS_OUT = REPO / "dbt" / "models" / "views"
COMPILED_OUT = REPO / "views" / "compiled"

HEADER = (
    "-- GENERATED by `python -m fhirhouse_views.dbt_gen` from views/definitions/{src}\n"
    "-- Do not hand-edit; edit the ViewDefinition and regenerate (FH-0005: the\n"
    "-- ViewDefinition is the only source artifact).\n"
)


def _source_sql(resource: str, jinja: bool) -> str:
    table = (f"{{{{ delta_table('silver', '{resource.lower()}') }}}}" if jinja
             else f"delta_scan('${{DELTA_BASE}}/silver/{resource.lower()}')")
    return (f"SELECT CAST(body_json AS JSON) AS resource, fhir_id AS resource_key\n"
            f"FROM {table}\nWHERE NOT coalesce(deleted, FALSE)")


def generate() -> list[str]:
    """Curated views (definitions/*.json) → models/views/; the generated base pack
    (definitions/base/*.json, from generate_base.py) → models/views/base/, which
    dbt_project.yml gates behind FHIRHOUSE_BASE_VIEW_PACK."""
    written = []
    for sub in ("", "base"):
        defs = DEFINITIONS / sub if sub else DEFINITIONS
        models_out = (MODELS_OUT / sub if sub else MODELS_OUT)
        compiled_out = (COMPILED_OUT / sub if sub else COMPILED_OUT)
        models_out.mkdir(parents=True, exist_ok=True)
        compiled_out.mkdir(parents=True, exist_ok=True)
        for f in sorted(defs.glob("*.ViewDefinition.json")):
            view = json.loads(f.read_text())
            name = view["name"]
            resource = view["resource"]
            src_rel = f"{sub + '/' if sub else ''}{f.name}"
            # dbt model (typed columns for BI; jinja source binding)
            compiled = ViewCompiler(view, typed=True).compile(_source_sql(resource, jinja=True))
            model = models_out / f"{name}.sql"
            model.write_text(HEADER.format(src=src_rel) + "\n" + compiled.sql + "\n")
            # plain-DuckDB compile target (illustration/ad hoc, no jinja)
            plain = ViewCompiler(view, typed=True).compile(_source_sql(resource, jinja=False))
            (compiled_out / f"{name}.duckdb.sql").write_text(
                HEADER.format(src=src_rel) + "\n" + plain.sql + ";\n")
            written.append(str(model.relative_to(REPO)))
    return written


def main() -> int:
    for m in generate():
        print(f"[dbt-gen] wrote {m}")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
